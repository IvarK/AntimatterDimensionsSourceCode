<script>
import PrimaryToggleButton from "@/components/PrimaryToggleButton";

export default {
  name: "ModalOptionsToggleButton",
  components: {
    PrimaryToggleButton
  },
  props: {
    isWide: {
      type: Boolean,
      required: false,
      default: true,
    },
    value: {
      type: Boolean,
      required: false,
      default: false,
    },
    text: {
      type: String,
      required: true
    }
  },
  computed: {
    classObject() {
      return {
        "o-primary-btn--option": !this.isWide,
        "o-primary-btn--option-wide": this.isWide,
      };
    },
    styleObject() {
      return {
        "background-color": this.value ? "var(--color-good)" : "var(--color-gh-purple)",
      };
    },
  }
};
</script>

<template>
  <PrimaryToggleButton
    :value="value"
    :label="text"
    :class="classObject"
    :style="styleObject"
    @input="emitInput"
  />
</template>
